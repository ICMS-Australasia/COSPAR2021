<p>In light of the advice from the Australian Federal Government and with the current travel bans and need to safeguard our space research community at large, COSPAR 2021 will continue as planned, but with the addition of a major on-line component enabling the Assembly to become a hybrid in-person / virtual event.</p>
<p>The Assembly will still be held at the same venue, the International Convention Centre Sydney, from Thursday 28 January 2021 through to Thursday 4 February 2021.</p>
<p>The very extensive and indeed excellent program that our Scientific Program Committee assembled for COSPAR-2020 will be carried over to the new dates, along with the line-up of inspirational international and national speakers, the interdisciplinary workshops, as well as immersive social opportunities.</p>
<p>We are developing an approach that guarantees that every Interdisciplinary Speaker, oral presentation, poster presentation and workshop will occur at least once in a time zone friendly to attendees.  Presentations will be a mix of in-person presentations from Sydney, live streaming and pre-recorded sessions.</p>
<p>The new hybrid format will enable all presenters to have the unique opportunity of sharing their science to a much broader audience.  COSPAR 2021 in-person attendees will follow the current program as scheduled.</p>
<p>The COSPAR community is well-positioned to respond positively to the changing environment and to utilise collaborative technology and with that, have wider access to information and a global research base. The ability to combine our traditional in-person meeting with an online virtual forum means that location, distance and isolation is now less of a barrier for participation. The preparations for this will enable full flexibility over the balance of virtual and in-person, in step with the uncertainties around COVID. Knowledge-sharing will remain the cornerstone of the Assembly and your engagement and contributions will be paramount to the success of the delivery. Heightening your experience will include the legacy of all sessions being recorded and uploaded as on-demand content; allowing you to experience all sessions, as you desire, and extending the value, impact and legacy of the worldâ€™s largest and leading space research forum and community.</p>
<p>We look forward to continuing to increase these outcomes as we adapt to the shifting events landscape and the global impact for our COSPAR space community. Rather than settle for a low-key Assembly under the constraints of COVID, we are excited about embracing the future and making the gathering even more successful than previously imagined.</p>
<p>As planning evolves, we will make regular announcements, via the website and social media platforms <a href="https://www.facebook.com/CommitteeOnSpaceResearch/?ref=bookmarks" target="_blank" style="color:#0c4da2;">@CommitteeonSpaceResearch</a> and <a href="https://www.linkedin.com/in/russell-boyce-935ab01a/?originalSubdomain=au" style="color:#0c4da2;" target="_blank">@LinkedIn</a>, providing the virtual participants in COSPAR-2021 with all the information they need to enjoy the Assembly to the fullest.</p>
<p>We are committed to ensuring that the health and safety of all of our speakers, sponsors, exhibitors, delegates and stakeholders is prioritised above all else.  Together we look forward to hosting COSPAR 2021 and in delivering a successful and impactful Assembly, remembered for many years to come.</p>
<p>Thanks for your confidence and your support.</p>





