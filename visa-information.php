                        <h2 class="mb-3 mt-5">Visa Options</h2>
                            <p>The most common Visa’s that may be recommended to you for short travel to Australia to attend a conference or congress are;</p>
                            <h3 class="mt-2">1. ETA (601)</h3>
                            <ul>
                              <li>Online Application Process</li>
                              <li>Quick</li>
                            </ul>
                            <p>Eligibility for an eVisitor visa and ETA will be determined by the country of passport that the applicant holds. The ETA is a quick, online, charge free process, but incurs a AUD$20 service provider charge, and/or a further charge if applied for through a travel agency, airline agency or specialist service provider. Further information on the ETA can be found on the <a target="_blank" href="https://immi.homeaffairs.gov.au/visas/getting-a-visa/visa-listing/electronic-travel-authority-601">website</a>.</p>
                            <h3 class="mt-2">2. eVisitor (651)</h3><ul>
                              <li>Online Application Process</li>
                              <li>Quick</li>
                              <li>One working day processing time</li>
                              <li>No Application charge or service fee</li>
                            </ul>
                            <p>Eligibility for an eVisitor visa and ETA will be determined by the country of passport that the applicant holds. The eVisitor visa is an online visa process, with a one working day processing time for low risk applications. There is no visa application charge or service fee. Further information on the eVisitor visa, including how to apply online, can be found on the <a target="_blank" href="https://immi.homeaffairs.gov.au/visas/getting-a-visa/visa-listing/evisitor-651">website</a>.</p>
                            <h3 class="mt-2">3. Visitor Visa (subclass 600) in the Business Visitor stream</h3>
                                <ul>
                              <li>Online or Paper application Process</li>
                            </ul>
                                <p>Passport holders who are not eligible to apply for an eVisitor or an ETA should apply for a Visitor Visa (subclass 600) in the Business Visitor Stream.</p>
                                <ul>
                                    <li>Online application process</li>
                                    <li>Application fee of $140 AUD</li>
                                </ul>
                                <p>Further information on the Subclass 600 Visa can be found on the <a href="https://immi.homeaffairs.gov.au/visas/getting-a-visa/visa-listing/visitor-600/business-visitor-stream" target="_blank">website</a>.</p>


                        <h2 class="mb-3 mt-5">Things to consider before lodging an application</h2>

                        <div class="row">
                        <div class="col-md-6">

                            <h3 class="mt-2">Invitation and registration to attend the event</h3>
                                <p>Although this is a separate process to lodging your visa application, when assessing whether you meet the criteria for the grant of a visa, the department may take into account whether you are registered or were invited to attend the event.</p>
                                <p>Confirmation of registration, or a copy of your letter of invitation, should accompany visa applications. For online subclass 400 and subclass 600 applications, information about documents that need to be attached will be provided as part of the application process.</p>
                                <p>To request a letter of invitation please contact the secretariat at <a target="_blank" href="mailto:registration@cospar2021.org">registration@cospar2021.org</a>.</p>

                                <h3 class="mt-2">Cut-off date for lodging paper based applications</h3>
                                <p>You should apply for a visa at least two months prior to the intended date of departure, and four months prior if there are any character or health concerns. More information about visa processing times is available on the department’s <a target="_blank" href="http://www.homeaffairs.gov.au/about/access-accountability/service-standards">website</a>.</p>

                                <h3 class="mt-2">Health and character requirements</h3>
                            <p>All visa applicants must be assessed against Australia's health and character requirements. These requirements are designed to protect the safety and security of the Australian community.</p>

                                <h3 class="mt-2">Health requirements</h3>
                                <p>As part of the visa application process you may be required to undertake a medical examination, chest x ray and/or other health checks.</p>
                                <p>The Department of Immigration and Border Protection (the department) will advise you of the applicable health checks (if any), based on your individual circumstances. Information on which health checks may apply to you is available on the department’s <a target="_blank" href="http://www.homeaffairs.gov.au/Trav/Visa/Heal" target="">website</a>.</p>

                                <h3 class="mt-2">Character requirements</h3>
                                <p>As part of the visa application assessment process the onus is on you to satisfy the character requirements set out in Section 501 of the Migration Act 1958. Further information on the character requirement is available on the department’s <a target="_blank" href="http://www.homeaffairs.gov.au/Trav/Visa/Char">website</a>.</p>
                                <p>If you are unsure whether you satisfy the character requirements because of prior criminal convictions, you should lodge a paper-based application at the Australian visa office nearest to your current place of residence, at least four months prior to the event start date.</p>
                                <p>Contact information for Australian visa offices is available on the department’s <a target="_blank" href="http://www.homeaffairs.gov.au/about/contact/offices-locations">website</a>.</p>


                            </div>

                            <div class="col-md-6">

                            <h3 class="mt-2">Travel sanctions</h3>
                            <p>For information on countries subject to travel sanctions, please refer to the Department of Foreign Affairs and Trade’s <a target="_blank" href="http://www.dfat.gov.au/international-relations/security/sanctions/pages/sanctions.aspx">website</a>. Nationals of these countries may not be permitted to travel to and enter Australia.</p>

                                <h3 class="mt-2">Biometrics Programme</h3>
                                <p>Biometrics may need to be collected at an Australian Visa Application Centre for visitors and business people lodging a visa application from the countries listed on the department’s <a target="_blank" href="http://www.homeaffairs.gov.au/Trav/Visa/Biom">website</a>.</p>
                                <p>If you lodge your application online, the department will advise you in writing if you need to provide your biometrics.</p>

                                <h3 class="mt-2">Obligations of visa holders</h3>
                                <p>On arrival at Australia’s border you must identify yourself, present your travel documents, and complete an Incoming Passenger Card (IPC). The IPC requires visitors to declare their health status and any prior criminal convictions.</p>
                                <p>If you do not meet immigration clearance requirements, you may be refused entry to Australia.</p>
                                <p>More information on immigration clearance processes at Australia’s borders is available on the department’s <a target="_blank" href="http://www.homeaffairs.gov.au/Busi/cargo-support-trade-and-goods/complying-with-your-obligations">website</a>.</p>
                                <p>You must comply with your visa validity and visa conditions while in Australia. When you receive your Australian visa you are issued with a visa grant notice that explains the conditions of the visa including period of validity and entry requirements. You are advised to carry this visa grant notice when travelling for your own reference.</p>
                                <p>Your visa may be cancelled if you breach any of the conditions.</p>
                                <p>If your visa expires while you are still in Australia, you become an unlawful non-citizen. Unlawful non-citizens are liable to be detained and removed from Australia.</p>

