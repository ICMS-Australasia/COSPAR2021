                        <p>All visitors to Australia must have a valid visa to travel to and enter Australia (other than New Zealand passport holders who will normally be granted a Special Category visa on arrival, provided they meet health and character requirements; and permanent residents of Norfolk Island who may be granted a Permanent Resident of Norfolk Island visa on arrival).</p>

                        <p>If you are planning to visit Australia for a business event such as a conference, trade fair, seminar or expo, there may be different visa options available to you. The appropriate visa will depend, amongst other things, on your purpose for visiting Australia.</p>

                        <div class="alert alert-primary pt-4 pb-4 mt-4" role="alert">
                          <strong>To explore visa options and get information on visa’s appropriate for you, visit the <a target="_blank" href="https://immi.homeaffairs.gov.au/visas/getting-a-visa/visa-finder/visit">Department of Home Affairs website</a> and answer a few short questions.
