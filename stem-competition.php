                                <h2>Cultural Recognition</h2>
                                <h3>We acknowledge the Traditional Owners of country throughout Australia and recognise their continuing connection to land, waters and culture. We pay our respects to their Elders past, present and emerging.</h3>
                                <p>The competition theme and guidelines have been developed in consultation with Aboriginal members of the NSW Department of Education and with Corey Tutt. We thank them all for their time and expertise and urge all our participants to be respectful while also taking joy in learning about our incredible Ancient Aboriginal Astronomy, its history and it’s present.</p>
                                <p>All artwork submitted that contains Aboriginal and/or Torres Strait Islander traditional art techniques, knowledge and/or information should be appropriately acknowledged. To pay respect and ensure cultural and intellectual property rights please complete the following acknowledgement where the submitted artwork uses any traditional art techniques or knowledge and information from a specific region or community. Please state technique and region or communities where it is considered a traditional practice.</p>

                                <p><em>For example:</em> <br>
                                    In this artwork I have used [state traditional technique]. This traditional [painting technique] is used by the [community name or region]. <br>
                                    In my artwork I have referenced the traditional story of [name of story]. The ancient story forms part of the [community name or region] cultural heritage.</p>

                                <p>If your artwork does not reference traditional aboriginal techniques, knowledge or information you are not required to submit this acknowledgement.</p>
