                        <h2 class="mb-3">Parkes Observatory – The Dish</h2>
                        <p>
                            <a href="https://www.csiro.au/en/Research/Facilities/ATNF/Parkes-radio-telescope/Parkes-Observatory-Visitors-Centre/Hours-location" target="_blank"><strong>https://www.csiro.au/en/Research/Facilities/ATNF/Parkes-radio-telescope/Parkes-Observatory-Visitors-Centre/Hours-location</strong></a>
                        </p>
                        <p>Just outside the town of Parkes in the central-west region of New South Wales, about 380 kilometres from Sydney, is our Parkes radio telescope. It's one of three instruments that make up the Australia Telescope National Facility.</p>
                        <p>With a diameter of 64 metres, Parkes is one of the largest single-dish telescopes in the southern hemisphere dedicated to astronomy. It started operating in 1961, but only its basic structure has remained unchanged.</p>
                        <p>The surface, control system, focus cabin, receivers, computers and cabling have all been upgraded – some parts many times – to keep the telescope at the cutting edge of radio astronomy. The telescope is now 10 000 times more sensitive than when it was commissioned.</p>
                        <p>Parkes radio telescope was one of the first to be built as a big, movable dish. The moving part of the telescope weighs 1000 tonnes – as much as two Boeing 747s – but it is not fixed to the tower. Instead, the giant dish sits on top of the control tower with its own weight holding it down. From our dedicated viewing area you can watch as the dish moves, controlled by astronomers as they explore the Universe.</p>

