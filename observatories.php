                        <p>The Siding Spring site was selected in 1962 from many other possible locations because of the dark and cloud-free skies.</p>
                        <p>The Siding Spring Observatory at Coonabarabran, about 350km north-west of Sydney, became fully Australian owned in 2010 – it had begun in 1967 as a joint venture with the UK and was the first major telescope in the Southern Hemisphere.  Siding Spring Observatory also houses many telescopes from institutions across the world including, Korea, America, the UK, Poland, Hungary, Germany and Russia.</p>
                        <p>Siding Spring Observatory (SSO), on the edge of the Warrumbungle National Park near Coonabarabran, NSW, is Australia's premier optical and infrared astronomical observatory. Research carried out at SSO is varied, from probing the depths of the cosmos in search of "Dark Energy" to searching the Milky Way for other planets and signs of life. Nearly every night there's something new being done and new discoveries being made.</p>
                        <p>The Anglo-Australian Telescope there has made significant contributions to the discovery of exoplanets (alien worlds orbiting other stars) and held the record for finding the farthest object known to man on a number of occasions.</p>
                        <p>There is a visitors' gallery and exhibition area open to the public which also incorporates a café and souvenir shop.</p>


