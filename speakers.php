            <h2>The Earth-Moon System:  Our Past, Present, and Future</h2>

            <p>It has been 50 years now since a human first stepped on another planetary body and began exploration of a totally new environment. Some remember that initial step as a once-in-a lifetime event that brought a dramatic change of perspective and understanding. To others it represents just a waypoint in history that occurred two generations in the past. Regardless of how that first venture from the Earth to the Moon is viewed, the space science world of today is quite different from that of 50 years ago. The number of space faring nations participating in exploration has greatly expanded, and the community is irreversibly international in nature, talents, and objectives.  Technical capabilities of instruments and sensors have improved exponentially and will continue to push new boundaries, pose new questions, and stumble upon profound surprises. What have we learned about our home through exploration of the Moon? Are we prepared for the next 50 years?</p>
            <p>The Past. We now know that the Earth and Moon have been intimately linked since our violent origin billions of years ago. The Moon’s surface documents the subsequent formative period of the Earth-Moon system and provides a lasting record of that early era, one that has long since been transformed and lost on Earth.</p>
            <p>The Present. Similarly, we recognize that the Earth and the Moon will continue to uniquely share the same environment at 1 AU. This includes the same ongoing interaction with the residual flux of large and small pieces of solar system debris. The Earth and Moon are also both simultaneously awash in the Sun’s variable magnetic environment and each faces the same amount of energetic solar and galactic plasma and neutral particles.</p>
            <p>The Future. Humanity inhabits all continents of Earth, albeit with different degrees of comfort and prosperity. To meet more of humanity’s vital needs and demands (which will only grow) in the next century, the Moon presents a newly accessible “8th continent” for future exploration as well as potential resources. We must plan carefully and proceed wisely.</p>
