            <h2>New High Energy Astrophysics Involving Gravitational Waves and Satellite Observations</h2>

            <p>A new exploration of the Universe has recently started through gravitational-wave observations. On September 14, 2015, the first detection of gravitational waves from the coalescence of a binary system of black holes by the Advanced LIGO detectors marked the dawn of gravitational-wave astronomy. On August 17, 2017, the first observation of gravitational waves from the inspiral and merger of a binary neutron-star system by the Advanced LIGO and Virgo network, followed 1.7 s later by a weak short gamma-ray burst detected by the Fermi and INTEGRAL satellites initiated the most extensive world-wide observing campaign which led to the detection of multi-wavelength electromagnetic counterparts. Multi-messenger discoveries are revealing the enigmas of the most energetic transients in the sky, probing relativistic astrophysics, nuclear physics, nucleosynthesis, and cosmology. The talk will give an overview of the astrophysical implications of the gravitational-wave and multi-messenger observations, focusing on the key role of multi-wavelength satellites operating together with the current and future gravitational-wave detectors.</p>
            <p>Marica is president of the commission of Gravitational Wave Astrophysics of the International Astronomical Union and member of the Gravitational Wave International Committee. She is member of the Virgo Collaboration, where she coordinated the LIGO and Virgo collaboration electromagnetic follow-up program from 2014 to 2018. She was in the panel that announced the first gravitational-wave signal from the coalescence of two neutron stars at the NSF international press-release. She was listed in  “Nature’s 10” as one of the most impactful scientists of the year 2017. Her scientific interest lies in the (astro)physics governing emission, formation and evolution of black holes and neutron stars. Her research activity is aimed at developing the multi-messenger astronomy, which uses electromagnetic and gravitational-wave observations to probe the physics of the most energetic transient phenomena in the Universe.</p>
