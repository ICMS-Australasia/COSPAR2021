            <h2>Climate Change, the Ocean, the Cryosphere and Regional Sea-level Change</h2>

            <p>Climate change has become one of the most important economic, environmental and social challenges of the 21st century, with sea-level rise a key aspect. Today, the order of 100 million of people live within a metre of high tide level, and more people are moving towards the coast in both the developed and developing world. Historical and paleo observations, the advent of modern satellite and in situ ocean, cyosphere and climate observing systems and the development of improved ocean, ice sheet and climate models has greatly improved our understanding of contemporary sea-level change. There is now a reasonable understanding of the reasons for sea-level change over recent decades and since 1900, including the attribution of the observed change to the climatic drivers. There are important implication for the 21st century and beyond. Critically important for regional sea level around the globe is the changing structure of the oceans and glaciers, the role of the oceans and atmosphere in the future of the ice sheets of Antarctica and Greenland and the vertical movement of coastal regions. Projections for the 21st century indicate sea levels could rise by a metre or more for unmitigated emissions. Sea levels will not stop rising in 2100, even for the strongest mitigation scenario. Indeed, failure to mitigate our greenhouse gas emissions will lead to a world of catastrophic changes. Avoiding these changes will require significant, urgent and sustained mitigation of greenhouse gas emissions. But even with successful mitigation, society will have to adapt to that component of climate change we cannot avoid. As a result, sea-level rise will have major impacts around the world.</p>
