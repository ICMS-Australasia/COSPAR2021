            <h2>The Status of Hayabusa 2 Asteroid Explorer and Its Results</h2>

            <p>JAXA has completed the space mission of Hayabusa asteroid sample return from 2003 to 2010. The retrieved capsule contained a lot of particles originating from the S-type asteroid Itokawa. Hayabusa 2 project was initiated in 2011 for the sample return from a C-type asteroid Ryugu. The bus system is designed more robustly than the original. The spacecraft was launched by H2A rocket December 2014 and then achieved rendezvous with the asteroid in June 2018 after the orbital maneuver by ion engines in 3.5 years. Three robots of MINERVAs and MSCOT successfully landed on and sent back pictures of the asteroid surface in close-up.</p>
            <p>The detail localization and mapping of the surface identified several points for the touch-down and the sample collection. On February 2019 Hayabusa 2 bravely tried the first touch-down and scattered a lot of fragments, some of which might be collected by Hayabusa 2, from the surface. The spacecraft fired the impactor and made an artificial crater with 10m diameter in April 2019. The second touch-down was executed beside the new crater in July. Hayabusa 2 will come back Earth at Woomera Australia on December 2020.</p>
