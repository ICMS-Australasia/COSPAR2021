        <h2 class="mt-5 mb-3">How will it work?</h2>
        <p>The details below are intended to provide required information to everyone associated with the Scientific Program for COSPAR-2021-Hybrid</p>
        <p>The information describes how each scientific session will be delivered for participation worldwide and describes how poster sessions will be conducted and the responsibilities of poster presenters. All virtual presenters will be well assisted in preparing for and participating in COSPAR-2021-Hybrid. The plans for Assembly-wide events such as interdisciplinary lectures are provided, as well as preliminary information, where available, on related events such as cross-disciplinary workshops, career fairs, etc., that will be held in conjunction with the Assembly.</p>
        <p>A summary is provided of the opportunities that are available in COSPAR-2021-Hybrid for the virtual and in-person participants, as well as the approach that is being taken to registration fees in this hybrid context. Finally, the key dates between now and the Assembly are outlined.</p>

        <h2 class="mt-5 mb-3">COSPAR 2021 Hybrid, a true global Assembly</h2>

        <p>The following are the activities and actions that will occur to deliver a Scientific Program for COSPAR-2021-Hybrid that can be enjoyed, with active participation, worldwide. These activities and actions apply to the Scientific Program, which was established for COSPAR-2021, when it was postponed from COSPAR 2020. There is no change in the program (themes, sessions, layout), now that COSPAR-2021 is COSPAR-2021-Hybrid. </p>

        <ul class="list-space">
          <li><strong>The abstract submission portal will be re-opened from 16 September 2020</strong>, to allow authors whose content may have evolved in the period of the delay from 2020 to 2021 to update their abstracts, subject to
            maintaining the essential theme of the presentation and relevance to the Event in which they have already been scheduled.</li>
          <li><strong>An opportunity to submit new abstracts to the existing program will be provided</strong> in order to allow the members of the community previously unable to attend Sydney in 2020 in-person to now participate in the global
            COSPAR-2021-Hybrid event. After new abstracts are submitted, the relevant MSO and DO and the Program Committee will review the abstracts and determine how they can be accommodated within the existing program, or, given the flexibility
            that is provided by a hybrid Assembly, whether an extension of the Event or an additional Event is required.</li>
            <li><strong>All presentations will be pre-recorded by presenters</strong>, with the duration consistent as per the original program timings. These will need to be uploaded for inclusion in the virtual conference delivery platform and will be available at the beginning of the Assembly, searchable according to COSPAR Commissions and Panels, technical themes, and keywords, to registered delegates only.</li>
            <li><strong>A short (few minutes, with exact duration to be advised), single-slide summary presentation will also be pre-recorded and uploaded by each presenter.</strong></li>
            <li><strong>The program schedule is designed to deliver the full program across global time zones</strong>. Instead of the oral program on any given day being delivered as parallel quarter-day sessions of consecutive 15-30 minute live talks, spread out over the Australian (and Asian) working day hours and thus representing significant strain for international delegates in different time zones, the program for each day will be concentrated into a three-hour block early in the Australian/Asian day (suitable for the American time zones), and repeated again at the end of the Australian/Asian day and into the evening (suitable for the European, African, and Middle-Eastern time zones). In this way, <strong>all time zones will be catered for</strong>.</li>
            <ul>
                <li>Each three-hour block will be divided into four 45-minute quarters, representing the original parallel quarter-day sessions for that day. Each 45-minute session will begin with the short summary versions of the presentations in the order in which they have been designated for that session in the program. These can be delivered either live streamed by the presenter or by streaming the pre-recorded version, at the option of the presenter. Following the delivery of these summary presentations, live and interactive Q&A will be enabled via the platform and moderated by the session chair (selected by the Event MSO and DO). The three-hour block will be repeated later that day.</li>
                <li>Presenters will be invited to deliver their summary presentation and answer questions in either or both of the three-hour blocks on their allocated day, either interactively as a live stream or utilising their pre-recording. The Q&A content will be captured, flow from the first block to the second and be maintained as a thread throughout the event and beyond.</li>
              </ul>

          <li><strong>The delivery of posters will be handled virtually</strong>, in a similar manner to the oral presentations, but maintaining a sense of the atmosphere of poster session gatherings.</li>
          <ul>
            <li>Each poster presentation will be pre-recorded in the short summary form, including a single-slide version of the poster itself.</li>
            <li>The pre-recording and slide will be available via the platform throughout the Assembly.</li>
            <li>On the relevant day for each programmed poster session, those sessions will be delivered virtually following the three-hour oral blocks.</li>
            <li>The platform will present to the delegates the full array of posters for that session as thumbnails of each slide.</li>
            <li>Clicking on a poster thumbnail will stream the summary presentation for that poster, while an adjacent link will take the delegate into a unique live break-out room with the poster presenter and other interested delegates, and
              interaction with the presenter and amongst those other delegates will be enabled.</li>
          </ul>
          <li><strong>The on-line platform, which will be available to both in-person and virtual participants</strong>, will include personalised tailoring options to highlight to each participant the content and the other participants and
            participating organisations that might be of specific relevance and interest to them and enable direct connections to be easily made. With the use of the platform, the ‘corridor conversations’ that are so valuable for building
            relationship and partnership will be enabled – assisted by virtual meeting rooms - and in fact enhanced.</li>
        </ul>


