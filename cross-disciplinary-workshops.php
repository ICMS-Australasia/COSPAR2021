                <h2 class="mt-5 mb-4"><a href="https://cospar2020.gitlab.io/cloudcomputing/" target="_blank">Cloud Computing for Space Research</a></h2>
                <h3>Date: Tuesday 2 February 2021<br>Time: 09:30 - 13:00 and 14:30 - 18:00<br> Room: Cockle Bay Room, Level 2, ICC Sydney</h3>
                <p>As space becomes increasingly accessible due to the burgeoning global space industry, the volume of data produced by space research missions is expected to continue rising exponentially.</p>
                <p>Opportunities for accelerating scientific discovery come with challenges in scaling traditional scientific processing workflows. A possible model for addressing such needs is the adoption of cloud compute technologies and open source software. At
                    the same time, the deployment of cloud compute technologies can allow researchers to address calls for scientific reproducibility.</p>
                <p><strong>This COSPAR 2021 workshop will include tutorials and case studies addressing the following questions:</strong></p>
                <ol>
                    <li>What cloud compute offerings are available for space researchers to develop workflows?</li>
                    <li>How can we use open source software and cloud compute to massively scale scientific data processing?</li>
                    <li>How can scientists equip ourselves with the skills to take advantage of these technologies?</li>
                    <li>How can cloud compute and web technologies be used to engage the public in citizen science, and for enhancing STEAM* education?</li>
                </ol>
                <p>*Science, Technology, Engineering, the Arts and Mathematics (STEAM)</p>
