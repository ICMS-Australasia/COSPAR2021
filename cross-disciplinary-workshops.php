                <h2 class="mt-5 mb-4"><a href="https://cospar2020.gitlab.io/autonomy/" target="_blank">Autonomy for Future Space Science Missions</a></h2>
                <h3>Date: Monday  1 Feb 2021
                  <br> Time: 09:30 - 13:00 and 14:30 - 18:00
 <br>
                  Room: Cockle Bay Room, Level 2, ICC Sydney</h3>
                <p>The rise of machine learning (ML) and artificial intelligence (AI) techniques is creating opportunities for space science missions with unprecedented capabilities. By augmenting traditional rule-based decision making with AI techniques (such as
                    decision policies in deep reinforcement learning), robotic missions may become highly autonomous. Furthermore, ML advances will augment the capabilities of crews serving in extended space missions.</p>
                <p><strong>Questions to be explored in this COSPAR 2021 workshop include:</strong></p>
                <ol>
                    <li>How will ML/AI enable autonomous space science missions?</li>
                    <li>What platforms are available for low-power, high-throughput compute in space?</li>
                    <li>How can advances in ML augment human capabilities in crewed missions?</li>
                </ol>
