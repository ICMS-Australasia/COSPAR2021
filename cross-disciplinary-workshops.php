                <p>The widespread availability of machine learning (ML) technologies promises to disrupt scientific disciplines.</p>
                <p>Popular open source ML frameworks are not only useful for data-driven model fitting, but also for efficient computation of physics-based models. This COSPAR 2021 workshop is dedicated to showcasing use cases of ML technologies to observational and
