                <h3>Date: Wednesday 19 August 2020 <br>Time: 9.30am – 2.30pm<br> Room: Cockle Bay Room, Level 2, ICC Sydney</h3>
                <p>As space becomes increasingly accessible due to the burgeoning global space industry, the volume of data produced by space research missions is expected to continue rising exponentially.</p>
